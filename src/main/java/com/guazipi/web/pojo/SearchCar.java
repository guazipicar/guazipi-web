package com.guazipi.web.pojo;

public class SearchCar {
	
	private Integer id;
	private String city;
	private String brand;
	private Integer price;
	private String series;
	private String color;
	private String speedbox;
	private String orl;   //燃油类型
	private Integer type;
	private Integer state;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getBrand() {
		return brand;
	}
	public void setBrand(String brand) {
		this.brand = brand;
	}
	public Integer getPrice() {
		return price;
	}
	public void setPrice(Integer price) {
		this.price = price;
	}
	public String getSeries() {
		return series;
	}
	public void setSeries(String series) {
		this.series = series;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getSpeedbox() {
		return speedbox;
	}
	public void setSpeedbox(String speedbox) {
		this.speedbox = speedbox;
	}
	public String getOrl() {
		return orl;
	}
	public void setOrl(String orl) {
		this.orl = orl;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	
}
